public class Kucing {
    private String id;
    private String nama_kucing;
    private String gender_kucing;
    private String jenis_kucing;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama_kucing() {
        return nama_kucing;
    }

    public void setNama_kucing(String nama_kucing) {
        this.nama_kucing = nama_kucing;
    }

    public String getGender_kucing() {
        return gender_kucing;
    }

    public void setGender_kucing(String gender_kucing) {
        this.gender_kucing = gender_kucing;
    }

    public String getJenis_kucing() {
        return jenis_kucing;
    }

    public void setJenis_kucing(String jenis_kucing) {
        this.jenis_kucing = jenis_kucing;
    }
}
